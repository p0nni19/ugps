package com.shipping.ugps.request;

public class LocationRequest {
    private double lat;
    private double longi;
    
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public double getLongi() {
		return longi;
	}
	public void setLongi(double longi) {
		this.longi = longi;
	}
}
